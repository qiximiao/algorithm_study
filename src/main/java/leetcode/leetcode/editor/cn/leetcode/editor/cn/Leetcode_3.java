public class Leetcode_3 {

    public int lengthOfLongestSubstring(String s) {
        int max=s.length();
        int l=0;
        int res=0;
        HashSet<Character> set = new HashSet<>();
        for(int r=0;r<max;r++){
            if(!set.contains(s.charAt(r))){
                res=Math.max(res,r-l+1);
                set.add(s.charAt(r));
            }else{
                while(s.charAt(r)!=s.charAt(l)){
                    set.remove(s.charAt(l));
                    l++;
                }
                l++;
                res=Math.max(res,r-l+1);
            }

        }
        return res;
    }
}
